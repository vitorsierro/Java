package pct_fonteCalculadora2;

public class Operacao {
	public  int soma(int valor01,int valor02)
	{
	return 	valor01 + valor02;
	}
	public  int subt(int valor01,int valor02)
	{
		return valor01 - valor02;
	}
	public  int mult(int valor01,int valor02)
	{
	return  valor01 * valor02;
	}
	public  int div(int valor01,int valor02)
	{
	return valor01 / valor02;	 
	}


}
